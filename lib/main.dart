          titleLarge: TextStyle(
            fontFamily: 'OpenSans',
            fontSize: 18,
            fontWeight: FontWeight.bold,
            color: Colors.black,
          ),
        ),
        appBarTheme: AppBarTheme(
          backgroundColor: Colors.purple, // Define a cor de fundo do AppBar
          titleTextStyle: TextStyle(
            fontFamily: 'OpenSans',
